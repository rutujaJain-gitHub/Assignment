number_one = 50
number_two = 10

print("Bitwise AND operator, number_one & number_two= ", number_one & number_two)

print("Bitwise OR operator, number_one | number_two= ", number_one | number_two)

print("Bitwise NOT operator, ~number_one = ", ~number_one)

print("Bitwise XOR operator, number_one ^ number_two= ", number_one ^ number_two)

print("Bitwise right shift, number_one >> 1= ", number_one >> 1)
print("Bitwise right shift, number_two >> 1= ", number_two >> 1)

print("Bitwise left shift, number_one << 1= ", number_one << 1)
print("Bitwise left shift, number_two << 2= ", number_two << 2)



